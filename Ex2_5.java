package Ex02Variable;

//예제 2_3의 변형
public class Ex2_5 {
	public static void main(String[] args) {
		
		int x = 10; // 변수에 다른 값을 넣어주기만 한다면 일일히 식을 고칠 필요가 없다.
		int y = 5;
		
		System.out.println(x+y); // 15
		System.out.println(x-y); // 5
		System.out.println(x/y); // 2
		System.out.println(x*y); // 50
		
	}

}
