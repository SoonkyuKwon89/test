package Ex02Variable;
/*
 * 화면에 글자 출력하기 - print() , println()
 * 화면에 글자를 출력할 때에는 System.out.print()를 사용한다.
 * 괄호안에는 출력하고자 하는 글씨를 넣으면 된다.
 * 줄바꿈을 하고싶을때는 println() , 줄바꿈을 안하고 싶을때는 print()를 사용한다. 
 */
public class Ex2_1 {
	public static void main(String[] args) {
		System.out.println("Hello, world!"); // 화면에 문자를 출력 후 줄바꿈
		System.out.print("hello"); // 화면에 hello 출력 후 줄바꿈X
		System.out.println("world");//화면에 world를 출력후 줄바꿈
		
	}

}
