package Ex02Variable;
/*
 * 사칙연산 계산하기
 */
public class Ex2_3 {
	public static void main(String[] args) {
		
		System.out.println(5+3);//화면에 결과값인 8이 출력된다.
		System.out.println(5-3);//화면에 결과값인 2가 출력된다.
		System.out.println(5/3);//화면에 결과값인 1이 출력된다.(나머지는 생략)
		System.out.println(5*3);//화면에 결과값인 15가 출력된다.
	}

}
