package Ex02Variable;
/*
 * 괄호 안에 숫자식 계산을 넣으면 계산된 값이 출력된다.
 * 만약 숫자 계산식 자체를 출력하고 싶다면 큰따옴표 사이에 넣으면 된다.
 */
public class Ex2_2 {
	public static void main(String[] args) {
		
		System.out.println("hello, world!");
		System.out.print("3 + 5 = "); // 숫자식 자체를 입력하고 싶을 때
		System.out.println(3+5);//계산된 값인 8이 출력된다
	}

}
